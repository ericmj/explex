defmodule Hex.Crypto.HmacTest do
  use ExUnit.Case, async: true

  test "hmac with sha256" do
    assert Hex.Crypto.hmac(:sha256, "QHCGZCQKH6PIUUWO", "data") ==
             <<32, 51, 233, 221, 234, 70, 234, 194, 219, 88, 63, 187, 117, 145, 88, 224, 158, 158,
               228, 148, 216, 145, 195, 170, 103, 155, 242, 57, 255, 220, 78, 143>>
  end

  test "hmac with sha384" do
    assert Hex.Crypto.hmac(:sha384, "QEH6ALDXO442FUQLQ2KAKQPS", "data") ==
             <<111, 252, 57, 12, 198, 83, 107, 79, 67, 69, 36, 247, 148, 248, 11, 42, 113, 139,
               161, 150, 73, 87, 175, 253, 2, 123, 89, 110, 184, 28, 139, 58, 155, 208, 42, 89,
               228, 102, 144, 226, 25, 85, 26, 225, 74, 222, 66, 160>>
  end

  test "hmac with sha512" do
    assert Hex.Crypto.hmac(:sha512, "QHCGZCQKH6PIUUWOQHCGZCQKH6PIUUWO", "data") ==
             <<164, 102, 74, 113, 144, 12, 184, 128, 41, 103, 198, 216, 197, 97, 175, 53, 178, 67,
               95, 179, 70, 20, 240, 124, 75, 9, 85, 208, 215, 133, 117, 2, 159, 85, 165, 129,
               164, 70, 237, 156, 248, 9, 50, 82, 15, 128, 160, 100, 192, 211, 173, 218, 161, 44,
               69, 164, 22, 251, 60, 153, 67, 188, 217, 42>>
  end
end
